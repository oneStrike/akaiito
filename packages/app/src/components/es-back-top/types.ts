export interface EsBackTopProps {
  /**
   * top属性的值，用于设置元素距离顶部的位置，可选
   */
  top?: number

  /**
   * duration属性的值，用于设置动画或过渡效果的持续时间，单位为毫秒，可选
   */
  duration?: number

  /**
   * zIndex属性的值，用于设置元素的堆叠顺序，可选
   */
  zIndex?: number

  /**
   * bottom属性的值，用于设置元素距离底部的位置，可选
   */
  bottom?: number

  /**
   * right属性的值，用于设置元素距离右侧的位置，可选
   */
  right?: number
}
