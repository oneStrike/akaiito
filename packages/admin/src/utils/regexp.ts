/**
 * 密码（密码至少包含大小写字母、数字、特殊字符、8~16位！）
 */
export const validPsw =
  /^(?=.*[0-9])(?=.*[A-Z])(?=.*[a-z])(?=.*[!_#%&@*.?+$^[\](){}|/\\])[0-9A-Za-z!_#%&@*.?+$^[\](){}|/\\].{7,15}$/

/**
 * 手机号
 */
export const validPhone = /^1[3-9]\d{9}$/

/**
 * 邮箱
 */
export const validEmail =
  /^(([^<>()[\]\\.,;:\s@"]+(\.[^<>()[\]\\.,;:\s@"]+)*)|(".+"))@((\[[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}])|(([a-zA-Z\-0-9]+\.)+[a-zA-Z]{2,}))$/
